#Notas do Bimestre e calculo da média

nota1=int(input("Qual foi a sua nota do 1ª Bimestre?:"))
nota2=int(input("Qual foi a sua nota do 2ª Bimestre?:"))
nota3=int(input("Qual foi a sua nota do 3ª Bimestre?:"))
nota4=int(input("Qual foi a sua nota do 4ª Bimestre?:"))
MediaAnual=float((nota1+nota2+nota3+nota4)/4)


print(f"Sua média anual foi de {MediaAnual} ")

